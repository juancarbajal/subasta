    /**
     * Permite realizar el analisis de una frase y devuelve tags con stemmer de Porter
     *
     * @param varchar $cadena
     * @return array
     */
    public function getTags($cadena) {
        // Paso 1, 2 y 3: Linealizacion y tokeniazacion de texto
        $palabras = preg_split('/[\®\/\«\»\.\\@\%\040]/', $this->filtroAlphaNum($cadena), -1, PREG_SPLIT_NO_EMPTY);
        // Paso 4: Stemmer a traves del algoritmo de Porter acoplado al idioma español
        foreach ($palabras as $hit) {
            if($this->getRelevantes($hit)) {
                $tags[] = PorterStemmer::stemm($hit);
            }
        }
        return $tags;
    }

